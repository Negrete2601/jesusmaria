		<h4>VALORES</h4>

		<p><span><strong>Honradez</strong></span></p>
		<p><span>Se refiere a la cualidad de actuar con rectitud, justicia y honestidad. En gobierno implica fomentar una cultura de integridad y transparencia, además de fortalecer la confianza entre los ciudadanos y sus instituciones.</span></p>

		<p><span><strong>Trabajo</strong></span></p>
		<p><span>Ello implica la creación y ejecución de acciones gubernamentales que indluye el tiempo y esfuerzo invertidos donde el trabajo humano es fundamental. La actividades en el ejercicio del servicio público reflejan el valor del trabajo aplicado al bienestar social.</span></p>

		<p><span><strong>Solidaridad</strong></span></p>
		<p><span>Es visto como un valor fundamental que implica que la cooperación y ell apoyo mutuo entre individuos y comunidades. En el contexto gubernamental se traduce en políticas y acciones que buscan el bienestar collectivo, promoviendo la inclusión y la justicia social.</span></p>

		<p><span><strong>Pluralismo</strong></span></p>
		<p><span>Es entendido como un concepto fundamental en las sociedades democráticas, que se refiere a la aceptación y reconocimiento de la diversidad de opiniones, creencias, y formas de vida. Aplicado al gobierno es asegurar la inclusión, la diversidad y la representación equitativa de todos los sectores de la sociedad.</span></p>	

		<p><span><strong>Resiliencia</strong></span></p>
		<p><span>Este valor no solo implica resistir las dificultades, sino también transformarse y aprender de ellas. En quehacer gubernamental se refiere a la capacidad de las instituciones para anticipar, adaptarse y recuperarse de eventos adversos, garantizando asi la continuidad y efectividad de sus funciones.</span></p>


		<h4>PRINCIPIOS</h4>

		<p><span><strong>Gobernanza</strong></span></p>
		<p><span>Facilita la cooperación entre diferentes sectores y asegura que las decisiones reflejen un equilibrio entre los intereses diversos de la sociedad. </span></p>

		<p><span><strong>Transparencia</strong></span></p>
		<p><span>Implica la apertura y accesibilidad de informacio relevante para los ciudadanos.</span></p>

		<p><span><strong>Innovación</strong></span></p>
		<p><span>Proceso de Introducir nuevas ideas, productos, servicios o métodos que generan un impacto positivo y aportan valor significativo.</span></p>

		<p><span><strong>Inclusión</strong></span></p>
		<p><span>Es la actitud, tendencia o política de integrar a todas las personas en la sociedad, con el objetivo de que puedan participar, contribuir y beneficiarse.</span></p>

		<p><span><strong>Subsidiariedad</strong></span></p>
		<p><span>Establece que las decisiones deben tomarse solo cuando los individuos o las comunidades no pueden resolver sus problemas por si mismos.</span></p>
		
		<p><span><strong>Priorizar</strong></span></p>
		<p><span>Implica el responder a los problemas y vulnerabilidades específicas de todas las personas, especialmente aquellas necesitadas y marginadas.</span></p>
